package Round1049;

public class AnotherDivisibilityProblem_2140B {
    public static void main(String[] args) throws Exception {

        FastScanner fs = new FastScanner(System.in);
        int t = fs.nextInt();

        while (t-- > 0) {
            int x = fs.nextInt();

            System.out.println(2 * x);
        }
    }
}
